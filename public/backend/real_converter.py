def allowed_file(filename):
    return '.' in filename and filename.rsplit('.', 1)[1].lower() in ALLOWED_EXTENSIONS

def estimate_depth(image):
    gray = image.convert('L')
    laplacian = gray.filter(ImageFilter.Kernel(
        (3,3), [-1,-1,-1, -1,8,-1, -1,-1,-1], scale=1, offset=128))
    return np.array(laplacian, dtype=np.float64)

def edge_detect(image):
    gray = np.array(image.convert('L'), dtype=np.int16)
    grad_x = np.abs(np.diff(gray, axis=1))
    grad_y = np.abs(np.diff(gray, axis=0))
    h, w = gray.shape
    edge_x = np.zeros((h, w))
    edge_x[:, :w-1] = grad_x
    edge_y = np.zeros((h, w))
    edge_y[:h-1, :] = grad_y
    magnitude = np.sqrt(edge_x**2 + edge_y**2)
    edges = (magnitude > 30).astype(np.uint8) * 255
    return edges

def generate_3d_model(edges, depth):
    vertices = []
    step = 5
    h, w = edges.shape
    for y in range(0, h, step):
        for x in range(0, w, step):
            if edges[y, x] > 0:
                z = depth[y, x] * 0.05
                vertices.append([float(x), float(y), float(z)])
    if not vertices:
        vertices.append([0.0, 0.0, 0.0])
    return vertices

def create_obj_file(vertices, filepath):
    with open(filepath, 'w') as f:
        f.write("# OBJ generated by Img3D\n")
        for v in vertices:
            f.write(f"v {v[0]} {v[1]} {v[2]}\n")

