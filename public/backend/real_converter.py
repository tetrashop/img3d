@app.route('/api/real-convert', methods=['POST'])
def real_convert():
    if 'image' not in request.files:
        return jsonify({'success': False, 'message': 'فایلی ارسال نشده'}), 400
    file = request.files['image']
    if file.filename == '' or not allowed_file(file.filename):
        return jsonify({'success': False, 'message': 'فرمت مجاز: png, jpg, jpeg, gif'}), 400

    original = secure_filename(file.filename)
    fname = f"{uuid.uuid4()}_{original}"
    filepath = os.path.join(app.config['UPLOAD_FOLDER'], fname)
    file.save(filepath)

    try:
        img = Image.open(filepath).convert('RGB')
        depth_map = estimate_depth(img)
        edges = edge_detect(img)
        vertices = generate_3d_model(edges, depth_map)

        obj_name = f"{uuid.uuid4()}.obj"
        obj_path = os.path.join(app.config['UPLOAD_FOLDER'], obj_name)
        create_obj_file(vertices, obj_path)

        return jsonify({
            'success': True,
            'model_url': f'/download/{obj_name}',
            'message': 'مدل 3D تولید شد'
        })
    except Exception as e:
        return jsonify({'success': False, 'message': str(e)}), 500
    finally:
        pass

@app.route('/download/<filename>')
def download_file(filename):
    # ارسال فایل از مسیر مطلق (بدون مشکل 404)
    return send_from_directory(app.config['UPLOAD_FOLDER'], filename, as_attachment=True)

